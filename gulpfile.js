var gulp = require('gulp');
var eslint = require('gulp-eslint');

// -----------------------------------------------------------------------------
// Configuration
// -----------------------------------------------------------------------------	

var paths = {
	dist: 'dist'
};

// -----------------------------------------------------------------------------
// Assets
// -----------------------------------------------------------------------------



// -----------------------------------------------------------------------------
// Testing
// -----------------------------------------------------------------------------
gulp.task('lint', function () {
	return gulp.src('src/**/*.js')
		.pipe(eslint({
			rules: {
				'quotes': 'single',
				'no-global-strict': 0,
				'no-console': 0
			},
			globals: {
				'require': true,
				'module': true
			},
			env: {
				browser: true
			}
		}))
		.pipe(eslint.format());
});

// -----------------------------------------------------------------------------
// Tasks
// -----------------------------------------------------------------------------
gulp.task('watch', function () {
	gulp.watch('src/{,**/}*.js', ['test']);
});

gulp.task('test', ['lint']);
gulp.task('default', ['test', 'watch']);